# Integers
num1 = 3.69
num2 = 2.5
n_division = num2 / num1
f_division = num2 // num1
m_division = num2 % num1
print(f'Normal division: {n_division}')
print(f'Floor division: {f_division}')
print(f'Modulo division: {m_division}')

# Multiplication
multi = num1 * num2
#Exponential
exp = num1 ** num2 # 3 raise to power 11

print(multi)
print(round(exp, 4))


